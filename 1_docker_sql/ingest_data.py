#!/usr/bin/env python
# coding: utf-8

import os
import argparse

from time import time

import pandas as pd
from sqlalchemy import create_engine


def main(params):
    user = params.user
    password = params.password
    host = params.host 
    port = params.port 
    db = params.db
    table_name = params.table_name
    url = params.url
    
    # the backup files are gzipped, and it's important to keep the correct extension
    # for pandas to be able to open the file
    if url.endswith('.csv.gz'):
        csv_name = 'output.csv.gz'
    else:
        csv_name = 'output.csv'

    os.system(f"wget {url} -O {csv_name}")

    engine = create_engine(f'postgresql://{user}:{password}@{host}:{port}/{db}')

    df_iter = pd.read_csv(csv_name, iterator=True, chunksize=100000)

    # Initialize total row counter
    

# Read the CSV file in chunks
    total_rows = 0

# Iterate through the CSV file in chunks
    for chunk in pd.read_csv(csv_name, compression='gzip', iterator=True, chunksize=100000):
        total_rows += chunk.shape[0]  # Add the number of rows in the current chunk

# Print the total number of rows in the entire file
    print(f"Total number of rows in the file: {total_rows}")
    df = next(df_iter)

    df.tpep_pickup_datetime = pd.to_datetime(df.tpep_pickup_datetime)
    df.tpep_dropoff_datetime = pd.to_datetime(df.tpep_dropoff_datetime)

    df.head(n=0).to_sql(name=table_name, con=engine, if_exists='replace')

    df.to_sql(name=table_name, con=engine, if_exists='append')

    total_chunks = total_rows // 100000
    
    chunks_remaining = total_chunks  # Subtract 1 for the first chunk already processed
    print(f"Chunks remaining: {chunks_remaining}")
    print("Frist chunk inserted!")

    start_time = time()


    while True: 

        try:
            t_start = time()
            df = next(df_iter)
            chunks_remaining -= 1  # Decrease the remaining chunks after processing each chunk
            print('chunks remaining: %d' % chunks_remaining)
            df.tpep_pickup_datetime = pd.to_datetime(df.tpep_pickup_datetime)
            df.tpep_dropoff_datetime = pd.to_datetime(df.tpep_dropoff_datetime)

            df.to_sql(name=table_name, con=engine, if_exists='append')

            t_end = time()

            print('inserted another chunk, took %.3f second' % (t_end - t_start))

        except StopIteration:
            total_time = time() - start_time
            print(f"Finished ingesting data into the PostgreSQL database")
            print(f"Total time taken: {total_time:.3f} seconds")
            break

if __name__ == '__main__':
    parser = argparse.ArgumentParser(description='Ingest CSV data to Postgres')

    parser.add_argument('--user', required=True, help='user name for postgres')
    parser.add_argument('--password', required=True, help='password for postgres')
    parser.add_argument('--host', required=True, help='host for postgres')
    parser.add_argument('--port', required=True, help='port for postgres')
    parser.add_argument('--db', required=True, help='database name for postgres')
    parser.add_argument('--table_name', required=True, help='name of the table where we will write the results to')
    parser.add_argument('--url', required=True, help='url of the csv file')

    args = parser.parse_args()

    main(args)